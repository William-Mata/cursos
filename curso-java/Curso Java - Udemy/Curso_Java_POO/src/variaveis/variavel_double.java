package variaveis;

public class variavel_double {

	public static void main(String[] args) {
	
	// ordem crescente dos reais: float, double.
	// double é o tipo primitivo padrão dos reais	
	double valor1 = 2.5;
	
	//cast de double para float.
	float valor2 = (float) (valor1);
	
	double valor3;
					
					
	valor3 = valor1 + valor2;
					
					
	System.out.println("Valor 01: " + valor1);		
	System.out.println("Valor 02: " + valor2);		
	System.out.println("Valor 03: " + valor3);
			

	}

}
