package entrada_saida_dados;

public class SaidaDados2 {

	public static void main(String[] args) {
		String nome = "william";
		int idade = 23;
		boolean casado = true;
		float salario = 1040.50f;
		char sexo = 'H';
		
		
		// Saida de dados formatada
		System.out.printf("nome: %s \n", nome);
		System.out.printf("idade: %d \n", idade );
		System.out.printf("salario: %f \n", salario);
		System.out.printf("casado: %b \n", casado);
		System.out.printf("sexo: %c \n", sexo);
		
		

	}

}
